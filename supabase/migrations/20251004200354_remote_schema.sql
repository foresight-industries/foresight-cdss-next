CREATE
OR REPLACE FUNCTION public.get_ar_aging_summary(p_team_id uuid, p_as_of_date date DEFAULT CURRENT_DATE)
 RETURNS TABLE(bucket character varying, claim_count bigint, total_amount numeric)
 LANGUAGE plpgsql
AS $function$
BEGIN
RETURN QUERY
SELECT CASE
         WHEN age_days <= 30 THEN '0-30'
         WHEN age_days <= 60 THEN '31-60'
         WHEN age_days <= 90 THEN '61-90'
         WHEN age_days <= 120 THEN '91-120'
         ELSE '120+'
