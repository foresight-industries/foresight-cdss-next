CREATE
OR REPLACE FUNCTION public.get_claim_readiness_score(p_claim_id character varying)
 RETURNS TABLE(readiness_score integer, blocking_issues text[], warnings text[])
 LANGUAGE plpgsql
AS $function$
DECLARE
v_score INTEGER := 100;
    v_blocking
TEXT[] := ARRAY[]::TEXT[];
    v_warnings
TEXT[] := ARRAY[]::TEXT[];
BEGIN
    -- Check validation
    IF
EXISTS (
        SELECT 1 FROM claim_validation
        WHERE claim_id = p_claim_id
        AND overall_status = 'error'
        ORDER BY validation_run_at DESC LIMIT 1
    ) THEN
        v_score := v_score - 50;
        v_blocking
:= array_append(v_blocking, 'Validation errors present');
END IF;

    -- Check timely filing
    IF
NOT (SELECT within_filing FROM check_timely_filing(p_claim_id)) THEN
        v_score := v_score - 100;
        v_blocking
:= array_append(v_blocking, 'Outside timely filing window');
END IF;

    -- Check scrubbing results
    IF
EXISTS (
        SELECT 1 FROM scrubbing_result
        WHERE entity_id = p_claim_id
        AND entity_type = 'claim'
        AND severity = 'error'
        AND fixed = false
    ) THEN
        v_score := v_score - 30;
        v_warnings
:= array_append(v_warnings, 'Unresolved scrubbing errors');
END IF;

RETURN QUERY SELECT
        GREATEST(v_score, 0),
        v_blocking,
        v_warnings;
END;
$function$
;

