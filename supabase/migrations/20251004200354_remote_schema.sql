CREATE
OR REPLACE FUNCTION public.calculate_next_retry(p_attempt_number integer, p_strategy character varying DEFAULT 'exponential'::character varying)
 RETURNS timestamp with time zone
 LANGUAGE plpgsql
AS $function$
DECLARE
v_minutes INTEGER;
BEGIN
    IF
p_strategy = 'exponential' THEN
        v_minutes := LEAST(POWER(2, p_attempt_number) * 5, 1440);
    ELSIF
p_strategy = 'linear' THEN
        v_minutes := p_attempt_number * 15;
ELSE
        v_minutes := 30;
END IF;

RETURN NOW() + (v_minutes || ' minutes')::INTERVAL;
END;
$function$
;

