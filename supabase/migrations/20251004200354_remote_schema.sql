CREATE
OR REPLACE FUNCTION public.accept_team_invitation(invitation_token text)
 RETURNS boolean
 LANGUAGE plpgsql
 SECURITY DEFINER
AS $function$
  DECLARE
invitation_record team_invitation%ROWTYPE;
    user_email
TEXT;
BEGIN
    -- Get current user email
SELECT email
INTO user_email
FROM auth.users
WHERE id = auth.uid();

-- Find valid invitation
SELECT *
INTO invitation_record
FROM team_invitation
WHERE token = invitation_token
  AND email = user_email
  AND expires_at > NOW()
  AND accepted_at IS NULL;

IF
NOT FOUND THEN
      RETURN FALSE;
END IF;

    -- Create team membership
INSERT INTO team_member (team_id, user_id, role, status, invited_by, invited_at, joined_at)
VALUES (invitation_record.team_id,
        auth.uid(),
        invitation_record.role,
        'active',
        invitation_record.invited_by,
        invitation_record.created_at,
        NOW());

-- Mark invitation as accepted
UPDATE team_invitation
SET accepted_at = NOW()
WHERE id = invitation_record.id;

-- Update user's current team if they don't have one
UPDATE user_profile
SET current_team_id = invitation_record.team_id
WHERE id = auth.uid()
  AND current_team_id IS NULL;

RETURN TRUE;
END;
  $function$
;

