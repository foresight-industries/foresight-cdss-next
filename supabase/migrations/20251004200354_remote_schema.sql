       JOIN encounter e ON c.encounter_id = e.id
       CROSS JOIN LATERAL (
  SELECT EXTRACT(DAY FROM p_as_of_date - e.dos) ::INTEGER as age_days
    ) ages
WHERE c.team_id = p_team_id
  AND c.status NOT IN ('paid', 'void', 'denied')
GROUP BY bucket
ORDER BY bucket;
END;
$function$
