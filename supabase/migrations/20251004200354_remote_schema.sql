 RETURNS trigger
 LANGUAGE plpgsql
 SECURITY DEFINER
AS $function$
DECLARE
record_data JSONB;
  record_id
TEXT;
  team_id_value
UUID;
BEGIN
  -- Get the record data
  IF
TG_OP = 'DELETE' THEN
    record_data := to_jsonb(OLD);
ELSE
    record_data := to_jsonb(NEW);
END IF;

  -- Extract ID (handle different ID field names)
  record_id
:= COALESCE(
    record_data->>'id',
    record_data->>'claim_id',
    (record_data->>'patient_id')::TEXT
  );

  -- Extract team_id if it exists (handle UUID type)
  IF
record_data ? 'team_id' AND record_data->>'team_id' IS NOT NULL THEN
    team_id_value := (record_data->>'team_id')::UUID;
END IF;

  -- Insert audit record
INSERT INTO audit_log (table_name,
                       operation,
                       team_id,
                       record_id,
                       old_data,
                       new_data)
VALUES (TG_TABLE_NAME,
        TG_OP,
        team_id_value,
        record_id,
        CASE WHEN TG_OP IN ('UPDATE', 'DELETE') THEN to_jsonb(OLD) ELSE NULL END,
        CASE WHEN TG_OP IN ('INSERT', 'UPDATE') THEN to_jsonb(NEW) ELSE NULL END);

IF
TG_OP = 'DELETE' THEN
    RETURN OLD;
ELSE
    RETURN NEW;
END IF;
END;
$function$
;

CREATE
OR REPLACE FUNCTION public.auto_post_era_payment(p_remittance_id uuid)
 RETURNS jsonb
 LANGUAGE plpgsql
AS $function$
DECLARE
v_result JSONB;
    v_posted_count
INTEGER := 0;
    v_review_count
INTEGER := 0;
BEGIN
WITH posted AS (
UPDATE payment_detail pd
SET status = 'posted'
WHERE pd.remittance_advice_id = p_remittance_id
  AND pd.status = 'pending'
  AND ABS(COALESCE(pd.paid_amount, 0) - COALESCE(pd.allowed_amount, 0)) < 0.01 RETURNING pd.id
    )
SELECT COUNT(*)
INTO v_posted_count
FROM posted;

WITH flagged AS (
UPDATE payment_detail pd
SET status = 'manual_review'
WHERE pd.remittance_advice_id = p_remittance_id
  AND pd.status = 'pending'
  AND ABS(COALESCE(pd.paid_amount, 0) - COALESCE(pd.allowed_amount, 0)) >= 0.01 RETURNING pd.id
    )
SELECT COUNT(*)
INTO v_review_count
FROM flagged;

v_result
:= jsonb_build_object(
        'auto_posted', v_posted_count,
        'manual_review', v_review_count,
        'processed_at', NOW()
    );

RETURN v_result;
END;
$function$
;

